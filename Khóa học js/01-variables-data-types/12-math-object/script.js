let x;

// Square root - Căn bậc hai
x = Math.sqrt(9);

// Absolute value - Giá trị tuyệt đối
x = Math.abs(-5);

// Round - Làm tròn
x = Math.round(4.2);

// Round up - Làm tròn lên
x = Math.ceil(4.2);

// Round down - Làm tròn xuống
x = Math.floor(4.9);

// Exponent - Lũy thừa
x = Math.pow(2, 3); // 2^3 = 8

// Minimum number - Số nhỏ nhất
x = Math.min(4, 5, 3);

// Maximum number - Số lớn nhất
x = Math.max(4, 5, 3);

// Get a random number/decimal between 0 and 1 - Lấy một số ngẫu nhiên từ 0 đến 1
x = Math.random();

// Get a random number between 1 and 100 - Lấy một số ngẫu nhiên từ 1 đến 100
x = Math.floor(Math.random() * 100 + 1);

console.log(x);
